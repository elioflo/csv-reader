public class Person {
  private String name;
  private int age;
  private char genero;

  public Person() {
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public char getGenero() {
    return genero;
  }

  public void setGenero(char genero) {
    this.genero = genero;
  }

  public void setAge(int age) {
    this.age = age;
  }
}
